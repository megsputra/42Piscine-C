char		*ft_strcapitalize(char *str)
{
	int i;
	int up;

	i = 0;
	up = 1;
	while (str[i])
	{
		if (str[i] == ' ' || str[i] == '+' || str[i] == '-')
			up = 1;
		else if (up == 1)
		{
			if (str[i] >= 97 && str[i] <= 122)
				str[i] -= 32;
			up--;
		}
		else if (up == 0)
		{
			if (str[i] >= 65 && str[i] <= 90)
				str[i] += 32;
		}
		++i;
	}
	return (str);
}
