int		ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int i;
	int r;

	i = 0;
	while (i < length - 1)
	{
		r = f(tab[i], tab[i + 1]);
		if (r > 0)
			return (0);
		i++;
	}
	return (1);
}
