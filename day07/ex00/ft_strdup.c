#include <stdlib.h>

int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	if (src[i] == '\0')
		dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	char *ret;

	ret = (char*)malloc(sizeof(*src) * (ft_strlen(src) + 1));
	ret = ft_strcpy(ret, src);
	return (ret);
}
