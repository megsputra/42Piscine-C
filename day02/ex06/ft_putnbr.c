int		ft_putchar(char c);

void	ft_print_dig(int *nb)
{
	if ((*nb / 1000000000 + '0') != '0')
		ft_putchar(*nb / 1000000000 + '0');
	if ((((*nb / 100000000) % 10) + '0') != '0')
		ft_putchar(((*nb / 100000000) % 10) + '0');
	if ((((*nb / 10000000) % 10) + '0') != '0')
		ft_putchar(((*nb / 10000000) % 10) + '0');
	if ((((*nb / 1000000) % 10) + '0') != '0')
		ft_putchar((((*nb / 1000000) % 10) + '0'));
	if ((((*nb / 100000) % 10) + '0') != '0')
		ft_putchar((((*nb / 100000) % 10) + '0'));
	if ((((*nb / 10000) % 10) + '0') != '0')
		ft_putchar((((*nb / 10000) % 10) + '0'));
	if ((((*nb / 1000) % 10) + '0') != '0')
		ft_putchar((((*nb / 1000) % 10) + '0'));
	if ((((*nb / 100) % 10) + '0') != '0')
		ft_putchar((((*nb / 100) % 10) + '0'));
	if ((((*nb / 10) % 10) + '0') != '0')
		ft_putchar((((*nb / 10) % 10) + '0'));
	if (((*nb % 10) + '0') != '0')
		ft_putchar((*nb % 10) + '0');
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		ft_putchar('1');
		ft_putchar('4');
		ft_putchar('7');
		ft_putchar('4');
		ft_putchar('8');
		ft_putchar('3');
		ft_putchar('6');
		ft_putchar('4');
		ft_putchar('8');
	}
	else
	{
		if (nb < 0)
		{
			ft_putchar('-');
			nb = -nb;
		}
		ft_print_dig(&nb);
	}
}
